`The National Motor Vehicle Title Information System (NMVTIS) is an electronic system that contains informationon certain automobiles titled in the United States.
NMVTIS is intended to serve as a reliable source of title and brand history for automobiles, but it does not contain detailed information regarding a vehicle’s repair history.

All states, insurance companies, and junk and salvage yards are required by federal law to regularly report information to NMVTIS. However, NMVTIS does not contain
information on all motor VINs in the United States because some states are not yet providing their vehicle data to the system. Currently, the data provided to 
NMVTIS by states is provided in a variety of time frames; while some states report and update NMVTIS data in “real-time” (as title transactions occur), other states 
send updates less frequently, such as once every 24 hours or within a period of days.

Information on previous, significant vehicle damage may not be included in the system if the vehicle was never determined by an insurance company (or other 
appropriate  entity) to be a "total loss" or branded by a state titling agency. Conversely, an insurance carrier may be required to report a "total loss" even if the vehicle’s 
titling-state has not determined the vehicle to be "salvage" or "junk."

A vehicle history report is NOT a substitute for an independent vehicle inspection. Before making a decision to purchase a vehicle, consumers are strongly encouraged 
to also obtain an independent vehicle inspection to ensure the vehicle does not have hidden damage. The Approved NMVTIS Data Providers (look for the NMVTIS logo)    
can include vehicle condition data from sources other than NMVTIS.

NMVTIS data INCLUDES (as available by those entities required to report to the System):

• Information from participating state motor vehicle titling agencies.

• Information on automobiles, buses, trucks, motorcycles, recreational VINs, motor homes, and truck tractors. NMVTIS may not currently include commercial VINs if 
those VINs are not included in a state’s primary database for title records (in some states, those VINs are managed by a separate state agency), although these 
records may be added at a later time.

• Information on “brands” applied to VINs provided by participating state motor vehicle titling agencies. Brand types and definitions vary by state, but may provide useful 
information about the condition or prior use of the vehicle.

• Most recent odometer reading in the state’s title record.

• Information from insurance companies, and auto recyclers, including junk and salvage yards, that is required by law to be reported to the system, beginning March 31, 
2009. This information will include if the vehicle was determined to be a “total loss” by an insurance carrier. 

• Information from junk and salvage yards receiving a “cash for clunker” vehicle traded-in under the Consumer Assistance to Recycle and Save Act of 2009 (CARS)
Program. 

Consumers are advised to visit vehiclehistory.bja.ojp.gov for details on how to interpret the information in the system and understand the meaning of various labels applied 
to VINs by the participating state motor vehicle titling agencies.`;
